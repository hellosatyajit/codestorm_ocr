"""OCR service layer.

Defines the `OCRService` Protocol that the orchestrator talks to, plus a
`MockOCRService` implementation that produces realistic-looking output
without requiring PaddleOCR to be installed.

Swap `MockOCRService` for a real one later by implementing the same
Protocol and passing it into `pipeline.process_document`.
"""
from __future__ import annotations

import random
from dataclasses import dataclass, field
from pathlib import Path
from typing import Protocol

import fitz  # PyMuPDF


@dataclass
class BlockResult:
    x0: float
    y0: float
    x1: float
    y1: float
    text: str
    confidence: float
    font_size_pt: float
    block_type: str = "paragraph"


@dataclass
class PageResult:
    index: int
    width_pt: float
    height_pt: float
    blocks: list[BlockResult] = field(default_factory=list)


class OCRService(Protocol):
    """Pluggable OCR contract.

    Real implementations (PaddleOCR, Tesseract, etc.) must implement
    `extract` and return a list of `PageResult` in page order.
    """

    def extract(self, source_path: Path) -> list[PageResult]: ...


